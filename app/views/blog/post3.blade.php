<h2 style="text-align: center">Decisive</h2>
<h3 style="text-align: center">Chip and Dan Heath</h3>
</p><p><p>
</p><p>This book is full of gold. The framework for this essay should hit every major focus of the book, apply it to a situation in my life and generalize it to the way that I should think about the world. 
</p><p>
</p><p>To begin, let’s talk about not understanding the problem. There are so many times where I spotlight on a particular frame for thinking about my decisions. One extremely important part of decision making seems to be recognising that a decision is being made. 
</p><p>
</p><p>The big spectre in so many of my thoughts is confirmation bias. Building the strongest arguments for both sides of every case is difficult. It does give me a much stronger understanding of the elements at work in my decision. My values are a lot clearer when the opposing argument is strengthened. It may even be worth re-framing the decision problem where the position that I was going to take is the opposing argument and the default is the thing I had an instinct against doing. 
</p><p>
</p><p>The idea that understanding my short-comings is not enough to fix them is important - I spend a lot of time impressed at my ability to understand myself, and very little time actively making improvements. I feel like I’m a lost cause a lot of the time, but the truth is that there are ways that I can grow that I should capitalize on. I need to take my understanding of myself and make improvements.
</p><p>
</p><p>The fact that the decision making process is far more important than the quality of analysis should lead me to make sure that I’m not blindly accepting ideas without consciously deciding. Following a process seems expensive - as one commenter noted, it seems like it’s really difficult to go through this entire process for every decision. It would be great to cut this process down to a 5-6 question heuristic algorithm for running a decision by a number of checks. I’ll attempt to do that as I write.
</p><p>
</p><p>These spotlight effects are pernicious, because a person doesn’t realize when they are spotlighting. There’s also a resistance that I always feel towards getting rid of my spotlight, because I feel like I’m going to have to do a lot of work to make a better decision. Obviously that work will pay greater dividends through me having avoided something expensive or done something really useful, but paying up-front costs to reap rewards later always feels bad during the up-front phase. This is a shortcoming of mine, a foible that I should notice and make efforts to avoid. 
</p><p>
</p><p>One issue with getting away from spotlighting is that it makes my much more indecisive. I’ll never have really considered all the options - it really simplifies the process to limit myself to a few key options, especially when the problem is already impossible to know the correct answer to and solve.
</p><p><h2 style="text-align: center"> Algorithmic Thinking</h2>
</p><p>Testing is much better than prediction. Can I do an experiment?
</p><p>What beliefs do I have that I should question?
</p><p>What is the evidence that contradicts my beliefs?
</p><p>What are the underlying desires guiding my decision?
</p><p>What are the options that we’re not considering?
</p><p>What objective information should inform our decision?
</p><p>Assume that some or all options are eliminated or required. What do we do?
</p><p>What is the opportunity cost of this decision?
</p><p>Can we reframe between prevention focus and promotion focus?
</p><p>Who else had this problem, and how can we learn from them?
</p><p>What evidence, if witnessed, would change your mind?
</p><p>When this when well, what was happening?
</p><p>What was the close-up experience of people who made my decision in the past?
</p><p>How will I feel about this decision in 5 minutes, in 4 months, in 3 years?
</p><p>Premortem - imagine this decision going horribly wrong and going extremely well. What caused it?
