       <h1>Philly Fusion</h1>
     </header>
     
     <p>The growth and development of Women’s Ultimate is something that I have been passionate about for the past few years and when the opportunity to partner with Brute Squad on a unique and innovative tournament arose, I jumped at it. Sara and I were very fortunate to have the support of all of our teammates, as well as friends like Greg Connelly, Carly Maconaghy, Leila Tunnell, Lindsey Hack, and all of the folks at USA Ultimate. The level of collaboration on this tournament was unlike anything I have ever experienced and it was truly a pleasure to work with all of the people involved.</p>

     <p>The vision for this tournament didn’t come overnight but was inspired by hours of brainstorming and lots of great ideas from friends.  While planning for the tournament, we also decided to create Without Limits, an initiative designed to empower Women's Ultimate players to build up the Women’s Ultimate community and create more quality playing and learning opportunities.  We had a great time planning Philly Fusion and watching the trade show and skills clinic happen was an incredible experience.</p>
     
     <p>In this feature, I've asked a variety of players in both the college and club divisions to share their experiences at Philly Fusion and to write about their vision for the Women's Division. My hope is that this feature challenges people to dream bigger and to find ways to create opportunities in their local Ultimate communities!</p>
     
     <p><strong>&mdash; Michelle Ng, <em>Philly Fusion Tournament Organizer</em></strong></p>

