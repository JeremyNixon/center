<p>Hume. Hume thinks more like me than any writer of philosophy that I've ever read. Any </p>

<p>rebuttal of his arguments is a rebuttal of my own. First to examine the ways that he's formalized </p>

<p>arguments that I've thought of, made and believe.</p>

<p>Hume's fundamental argument is that everything humans know stems from the fact the </p>

<p>experiences that we have had represent what will happen in the future. If I witness an event, </p>

<p>and see the same circumstances set up in future, I should expect the same result. This </p>

<p>continuity between past and future is essential for us to know how to interact with the world. I </p>

<p>would call it a fundamental axiom of thought. The past is like the future. We have innumerable </p>

<p>association of one cause with one effect. We generalize to the future and to similar situations. </p>

<p>Hume's sceptical premise is extremely important: that we do not believe things that we </p>

<p>do not have experiences to justify, or things that violate rules that hold in every single other </p>

<p>scenario. We also need evidence to smartly evaluate the truth or falsity of a claim. As evidence </p>

<p>increases we increase the certainty of our belief. We do not believe in the absence of evidence. </p>

<p>Evidence is experience in the world. </p>

<p>Hume spends a lot of space breaking down the arguments of those who believe in a </p>

<p>supreme being. One novel argument that stood out to me is that if we look at creation as the </p>

<p>effect of some force, and creation is the only metric we have to measure the magnitude of the </p>

<p>creating force, we cannot suppose that the creator is greater or more perfect than the creation. </p>

<p>So all of the imperfections we see in creation must be reflected in the creator, or at least we </p>

<p>cannot assume that he surpasses them.</p>

<p>Hume derides the 'history' that comes out of barbaric nations that talks of miracles and </p>

<p>gods, noting that the fool is quick to repeat and extemporize on tales that are heard, and that it </p>

<p>is easy to convince them of fantastic tales. As more and more people believe the tales and they </p>

<p>start being passed down by an ancestry, they start to be regarded as truth. But note the number </p>

<p>of supernatural events that have happened in civilised societies, with witnesses who are </p>

<p>capable of using reason and sound judgement. There are almost none. So we should distrust </p>

<p>these stories whose evidence contradicts physical law (by definition of miracle) and which is </p>

<p>insisted on by a mass of imaginative people refusing to think reasonably, but rather think with </p>

<p>what they want to be true and with their emotion. </p>

<p>If a deity has created all people, then no person is responsible for his actions, but the </p>

<p>deity who created him to be the kind of person who would act in such a way. </p>

<p>Hume finally demeans all fields that are not scientific or mathematical to a domain </p>

<p>outside reason. This is surprisingly similar to a conclusion I came to that technical skill and </p>

<p>mathematics are the only pure and true things that we know of, and that the power and certainty </p>

<p>of these fields is a purer growth of human ability than anything else.</p>