For the last couple of years, consumers have been the target of a tax scam where scammers 

pretending to be from the Internal Revenue Service (IRS) are calling people about supposedly 

delinquent accounts.  The Massachusetts Attorney General’s Office, the Massachusetts Office of 

Consumer Affairs and Business Regulation, and the local consumer programs have received 

many complaints about this ongoing scam. Additionally, several warnings have been posted by 

The Federal Trade Commission and IRS.  Be skeptical if you are contacted by someone 

purporting to be from the IRS. They may tell you that you owe the IRS money and  must pay up 

or face a serious fine or imprisonment.  Be extra skeptical, if you are asked to pay by putting 

money onto a prepaid card.  The IRS will never contact you by telephone or email.   To find out 

more about the IRS tax scam and what to do if you have received one of these calls check out the 

IRS website:  http://www.irs.gov/uac/Newsroom/Scam-Phone-Calls-Continue-IRS-Unveils-

New-Video-to-Warn-Taxpayers.
